/**
 * Convenience function to check if an address is a zero address.
 * @param {string} address - The address to check.
 * @returns {boolean} true if the address is a zero address, false otherwise.
 */
export default function isZeroAddress(address: string): boolean {
  return address === 'AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAY5HFKQ';
}
